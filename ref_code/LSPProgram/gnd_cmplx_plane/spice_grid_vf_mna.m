                            vfcplx_flag = ih+1;
                            
                            R1 = Rvfit3(ik,ih);
                            R2 = Rvfit3(ik,ih+1);
                            L1 = Lvfit3(ik,ih);
                            L2 = Lvfit3(ik,ih+1);
                            
                            LC = L1+L2;
                            RC2 = LC^2*R1*R2 / (R2*L1^2+R1*L2^2);
                            RC1 = 1 / (1/(R1+R2)-1/RC2);
                            CC = 1/RC1 * L1*L2/(R1*R2)*(R1+R2)/LC;
                            
                            fprintf(fid,'R_VC%d  nR_%d  nVC_%d  %em  TC=0,0 \n', ...
                                NV, ik, NV, 1e3*RC1);
                            fprintf(fid,'C_V%d  nVC_%d  nV_%d  %euF \n', ...
                                NV, NV, NV, 1e6*CC);
                            fprintf(fid,'R_V%d  nR_%d  nV_%d  %em  TC=0,0 \n', ...
                                NV, ik, NV, 1e3*RC2);
                            fprintf(fid,'L_V%d  nR_%d  nV_%d  %euH \n', ...
                                NV, ik, NV, 1e6*LC);
                        end
                        
                    elseif ih>1 && Nfit_grid>0 && ih~=vfcplx_flag
                        NV = NV+1;
                        
                        if isreal(Rvfit3(ik,ih))  % real pole
                            fprintf(fid,'R_V%d  nV_%d  nV_%d  %em  TC=0,0 \n', ...
                                NV, NV-1, NV, 1e3*Rvfit3(ik,ih));
                            fprintf(fid,'L_V%d  nV_%d  nV_%d  %euH \n', ...
                                NV, NV-1, NV, 1e6*Lvfit3(ik,ih));
                        else % complex pole
                            vfcplx_flag = ih+1;
                            
                            R1 = Rvfit3(ik,ih);
                            R2 = Rvfit3(ik,ih+1);
                            L1 = Lvfit3(ik,ih);
                            L2 = Lvfit3(ik,ih+1);
                            
                            LC = L1+L2;
                            RC2 = LC^2*R1*R2 / (R2*L1^2+R1*L2^2);
                            RC1 = 1 / (1/(R1+R2)-1/RC2);
                            CC = 1/RC1 * L1*L2/(R1*R2)*(R1+R2)/LC;
                            
                            fprintf(fid,'R_VC%d  nV_%d  nVC_%d  %em  TC=0,0 \n', ...
                                NV, NV-1, NV, 1e3*RC1);
                            fprintf(fid,'C_V%d  nVC_%d  nV_%d  %euF \n', ...
                                NV, NV, NV, 1e6*CC);
                            fprintf(fid,'R_V%d  nV_%d  nV_%d  %em  TC=0,0 \n', ...
                                NV, NV-1, NV, 1e3*RC2);
                            fprintf(fid,'L_V%d  nV_%d  nV_%d  %euH \n', ...
                                NV, NV-1, NV, 1e6*LC);
                        end
                        
                    end
                end
                
                %nodL1 = nodR;
                NL = NL+1;
                fprintf(fid,'L_L%d  nV_%d  %s  %euH \n', ...
                    NL, NV, nod_end_grid(ik,:), 1e6*Ls(ik));
            else
                
                NL = NL+1;
                fprintf(fid,'L_L%d  nR_%d  %s  %euH \n', ...
                    NL, ik, nod_end_grid(ik,:), 1e6*Ls(ik));
            end
        end
        
        %% K -- 2-a, 2-b use the same K writing part
        cofK(ik+1:Nbran) = Lgrid(ik+1:Nbran,ik)./sqrt(Ls(ik).*Ls(ik+1:Nbran));
        for ih = ik+1:Nbran
            if cofK(ih) > 1
                cofK(ih) = 0.99;
            elseif cofK(ih) < -1
                cofK(ih) = -0.99;
            end
            
            if abs(cofK(ih)) > err_ML
                NK = NK+1;
                fprintf(fid,'K_K%d  L_L%d  L_L%d  %.4f \n', ...
                    NK, ik, ih, cofK(ih));
            end
        end
        
    end
    
elseif Nbran==1
    %% R - 2-a, 2-b use the same R writing part
    
    Nfit_grid = find(Rvfit3(ik,:), 1, 'last');
    
    fprintf(fid,'R_R%d  %s  nR_%d  %em  TC=0,0 \n', 1, nod_sta_grid(1,:), 1, Rgrid(1,1)*1e3);
    
    %% 2-a. with current measurment
    if ~isempty(brn_out_grid) && ...
            ismember(brn_name_grid(1,:),brn_out_grid,'rows')
        
        % add current measurment block
        %nodM = ['nM_' num2str(ik)];
        fprintf(fid,'*  Mearsure Branch Current %s \n',brn_name_grid(1,:));
        % 0V Vdc
        fprintf(fid,'V_M%d  nR_%d  nM_%d  0Vdc \n', 1, 1, 1);
        % H (CCVS for measuring current)
        fprintf(fid,'H_M%d  %s  0  V_M%d  1 \n', 1, brn_name_grid(1,:), 1);
        fprintf(fid,'R_M%d  %s  0  1G  TC=0,0 \n', 1, brn_name_grid(1,:));
        %nodL1 = nodM;
        
        if Nfit_grid>0
            % add vector fitting equivalent circuit
            vfcplx_flag = Nfit_grid+1;
            
            for ih = 1:Nfit_grid
                
                if ih == 1
                    NV = NV+1;
                    
                    if isreal(Rvfit3(ik,ih))  % real pole
                        fprintf(fid,'R_V%d  nM_%d  nV_%d  %em  TC=0,0 \n', ...
                            NV, 1, NV, 1e3*Rvfit3(ik,ih));
                        fprintf(fid,'L_V%d  nM_%d  nV_%d  %euH \n', ...
                            NV, 1, NV, 1e6*Lvfit3(ik,ih));
                    else % complex pole
                        vfcplx_flag = ih+1;
                        
                        R1 = Rvfit3(ik,ih);
                        R2 = Rvfit3(ik,ih+1);
                        L1 = Lvfit3(ik,ih);
                        L2 = Lvfit3(ik,ih+1);
                        
                        LC = L1+L2;
                        RC2 = LC^2*R1*R2 / (R2*L1^2+R1*L2^2);
                        RC1 = 1 / (1/(R1+R2)-1/RC2);
                        CC = 1/RC1 * L1*L2/(R1*R2)*(R1+R2)/LC;
                        
                        fprintf(fid,'R_VC%d  nM_%d  nVC_%d  %em  TC=0,0 \n', ...
                            NV, 1, NV, 1e3*RC1);
                        fprintf(fid,'C_V%d  nVC_%d  nV_%d  %euF \n', ...
                            NV, NV, NV, 1e6*CC);
                        fprintf(fid,'R_V%d  nM_%d  nV_%d  %em  TC=0,0 \n', ...
                            NV, 1, NV, 1e3*RC2);
                        fprintf(fid,'L_V%d  nM_%d  nV_%d  %euH \n', ...
                            NV, 1, NV, 1e6*LC);
                    end
                    
                elseif ih>1 && Nfit_grid>0 && ih~=vfcplx_flag
                    NV = NV+1;
                    
                    if isreal(Rvfit3(ik,ih))  % real pole
                        fprintf(fid,'R_V%d  nV_%d  nV_%d  %em  TC=0,0 \n', ...
                            NV, NV-1, NV, 1e3*Rvfit3(ik,ih));
                        fprintf(fid,'L_V%d  nV_%d  nV_%d  %euH \n', ...
                            NV, NV-1, NV, 1e6*Lvfit3(ik,ih));
                    else % complex pole
                        vfcplx_flag = ih+1;
                        
                        R1 = Rvfit3(ik,ih);
                        R2 = Rvfit3(ik,ih+1);
                        L1 = Lvfit3(ik,ih);
                        L2 = Lvfit3(ik,ih+1);
                        
                        LC = L1+L2;
                        RC2 = LC^2*R1*R2 / (R2*L1^2+R1*L2^2);
                        RC1 = 1 / (1/(R1+R2)-1/RC2);
                        CC = 1/RC1 * L1*L2/(R1*R2)*(R1+R2)/LC;
                        
                        fprintf(fid,'R_VC%d  nV_%d  nVC_%d  %em  TC=0,0 \n', ...
                            NV, NV-1, NV, 1e3*RC1);
                        fprintf(fid,'C_V%d  nVC_%d  nV_%d  %euF \n', ...
                            NV, NV, NV, 1e6*CC);
                        fprintf(fid,'R_V%d  nV_%d  nV_%d  %em  TC=0,0 \n', ...
                            NV, NV-1, NV, 1e3*RC2);
                        fprintf(fid,'L_V%d  nV_%d  nV_%d  %euH \n', ...
                            NV, NV-1, NV, 1e6*LC);
                    end
                end
            end
            
            % L
            NL = NL+1;
            fprintf(fid,'L_L%d  nV_%d  %s  %euH \n', NL, NV, nod_end_grid(1,:), 1e6*Ls(1));
        else
            
            % L
            NL = NL+1;
            fprintf(fid,'L_L%d  nM_%d  %s  %euH \n', NL, 1, nod_end_grid(1,:), 1e6*Ls(1));
            
        end
        %% 2-b. no current measurment
    else
        Nfit_grid = find(Rvfit3(1,:), 1, 'last');
        %nodL1 = nodR;
        if Nfit_grid>0
            % add vector fitting equivalent circuit
            vfcplx_flag = Nfit_grid+1;
            
            for ih = 1:Nfit_grid
                if ih == 1
                    NV = NV+1;
                    
                    if isreal(Rvfit3(ik,ih))  % real pole
                        fprintf(fid,'R_V%d  nR_%d  nV_%d  %em  TC=0,0 \n', ...
                            NV, 1, NV, 1e3*Rvfit3(1,ih));
                        fprintf(fid,'L_V%d  nR_%d  nV_%d  %euH \n', ...
                            NV, 1, NV, 1e6*Lvfit3(1,ih));
                    else % complex pole
                        vfcplx_flag = ih+1;
                        
                        R1 = Rvfit3(ik,ih);
                        R2 = Rvfit3(ik,ih+1);
                        L1 = Lvfit3(ik,ih);
                        L2 = Lvfit3(ik,ih+1);
                        
                        LC = L1+L2;
                        RC2 = LC^2*R1*R2 / (R2*L1^2+R1*L2^2);
                        RC1 = 1 / (1/(R1+R2)-1/RC2);
                        CC = 1/RC1 * L1*L2/(R1*R2)*(R1+R2)/LC;
                        
                        fprintf(fid,'R_VC%d  nR_%d  nVC_%d  %em  TC=0,0 \n', ...
                            NV, 1, NV, 1e3*RC1);
                        fprintf(fid,'C_V%d  nVC_%d  nV_%d  %euF \n', ...
                            NV, NV, NV, 1e6*CC);
                        fprintf(fid,'R_V%d  nR_%d  nV_%d  %em  TC=0,0 \n', ...
                            NV, 1, NV, 1e3*RC2);
                        fprintf(fid,'L_V%d  nR_%d  nV_%d  %euH \n', ...
                            NV, 1, NV, 1e6*LC);
                    end
                    
                elseif ih>1 && Nfit_grid>0 && ih~=vfcplx_flag
                    NV = NV+1;
                    
                    if isreal(Rvfit3(ik,ih))  % real pole
                        fprintf(fid,'R_V%d  nV_%d  nV_%d  %em  TC=0,0 \n', ...
                            NV, NV-1, NV, 1e3*Rvfit3(1,ih));
                        fprintf(fid,'L_V%d  nV_%d  nV_%d  %euH \n', ...
                            NV, NV-1, NV, 1e6*Lvfit3(1,ih));
                    else % complex pole
                        vfcplx_flag = ih+1;
                        
                        R1 = Rvfit3(ik,ih);
                        R2 = Rvfit3(ik,ih+1);
                        L1 = Lvfit3(ik,ih);
                        L2 = Lvfit3(ik,ih+1);
                        
                        LC = L1+L2;
                        RC2 = LC^2*R1*R2 / (R2*L1^2+R1*L2^2);
                        RC1 = 1 / (1/(R1+R2)-1/RC2);
                        CC = 1/RC1 * L1*L2/(R1*R2)*(R1+R2)/LC;
                        
                        fprintf(fid,'R_VC%d  nV_%d  nVC_%d  %em  TC=0,0 \n', ...
                            NV, NV-1, NV, 1e3*RC1);
                        fprintf(fid,'C_V%d  nVC_%d  nV_%d  %euF \n', ...
                            NV, NV, NV, 1e6*CC);
