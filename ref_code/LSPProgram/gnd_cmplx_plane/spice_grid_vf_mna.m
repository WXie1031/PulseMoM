            end
            
            % L
            fprintf(fid,'L_L%d  nV_%d  %s  %euH \n', NL, NV, nod_end_grid(1,:), 1e6*Ls(1));
        else
            
            fprintf(fid,'L_L%d  nR_%d  %s  %euH \n', NL, 1, nod_end_grid(1,:), 1e6*Ls(1));
        end
    end
    
end


%% 3.b Yn off diagonal element
for ik = 1:Nnod-1
    fprintf(fid,'*  Node  %s  to other nodes\n', nod_grid(ik,:));
    
    for ig = ik+1:Nnod
        fprintf(fid,'* *  Node  %s  to  Node  %s  \n', nod_grid(ik,:),nod_grid(ig,:));
        
        Rtmp = -1/Ggrid(ik,ig);
        Ctmp = -Cgrid(ik,ig);
        
        %         NR = NR+1;
        %         fprintf(fid,'R_R%d  %s  %s  %em  TC=0,0 \n', ...
        %             NR, nod_grid(ik,:), nod_grid(ig,:), Rtmp*1e3);
        
        if (Ctmp)>1e-8
            NC = NC+1;
            fprintf(fid,'C_C%d  %s  %s  %euF \n', ...
                NC, nod_grid(ik,:), nod_grid(ig,:), 1e6*Ctmp);
        elseif (Ctmp)>1e-12
            NC = NC+1;
            fprintf(fid,'C_C%d  %s  %s  %epF \n', ...
                NC, nod_grid(ik,:), nod_grid(ig,:), 1e9*Ctmp);
        end
        
    end
end


%% 3.b Write the GND node
% Adding nonlinear ground resistance modeling soil ionization effect
E0 = 350e3;  % E = 350kV/m
RGname =[fname_grid,'_Ri',];
NRG = 0;

for ik = 1:Nnod
    fprintf(fid,'* *  Node %s to ground \n', nod_grid(ik,:));
    
    NRG = NRG+1;
    
    dum=Ggrid(ik,:);dum(ik)=0;
    D = Ggrid(ik,ik)+sum(dum);
    % replace by the nonlinear Rg
    Rgrod = 1/D;
    if Rgrod<0
        Rgrod = Rrod_dc(ik);
    elseif Rgrod>2*Rrod_dc(ik)
        Rgrod = Rrod_dc(ik);
    end
    
    if flag_Rg == 0
        if Rgrod~=0
            fprintf(fid,'R_R%s  %s  0  %e  TC=0,0 \n', ...
                nod_grid(ik,:), nod_grid(ik,:), Rgrod);
        end
    elseif flag_Rg == 1
        
        Ig = 1/(2*pi)*E0*Rsoil/(Rgrod^2);
        fprintf(fid,'X_G%s  %s  %s  PARAMS: Ig=%e  R0=%e \n', ...
            nod_grid(ik,:), nod_grid(ik,:), RGname, Ig, Rgrod);
    end
    
    dum=Cgrid(ik,:);dum(ik)=0;
    C0 = Cgrid(ik,ik)+sum(dum);
    
    if C0>1e-8
        NC = NC+1;
        fprintf(fid,'C_C%s  %s  0  %euF \n', nod_grid(ik,:), nod_grid(ik,:), C0*1e6);
    elseif C0>1e-12
        NC = NC+1;
        fprintf(fid,'C_C%s  %s  0  %epF \n', nod_grid(ik,:), nod_grid(ik,:), C0*1e9);
    elseif C0<0 && abs(C0)>1e-8
        NC = NC+1;
        fprintf(fid,'C_C%s  %s  0  %euF \n', nod_grid(ik,:), nod_grid(ik,:), Crod_dc(ik)*1e6);
    elseif C0<0 && abs(C0)>1e-12
        NC = NC+1;
        fprintf(fid,'C_C%s  %s  0  %epF \n', nod_grid(ik,:), nod_grid(ik,:), Crod_dc(ik)*1e9);
    end
    
end

fprintf(fid,'.ENDS %s \n', fname_grid);


%% 4. Adding nonlinear ground resistance
if flag_Rg == 1
    % single rod inozation model
    fprintf(fid,'* Nonlinear Ground Resistance Used in Ground Grid\n');
    fprintf(fid,'.SUBCKT  %s  1  PARAMS: Ig=1000  R0=20 \n', RGname);
    fprintf(fid,'R_R_ROD    1  2  1u  TC=0,0 \n');
    fprintf(fid,'V_I_SENSE  2  3  0V \n');
    fprintf(fid,'H_I_SENSE  4  0  V_I_SENSE   1 \n');
    fprintf(fid,'R_I_SENSE  4  0  1G   TC=0,0 \n');
    fprintf(fid,'E_VAR  3  0 \n');
    fprintf(fid,'+  VALUE={V(4)*R0/SQRT(1+ABS(V(4))/Ig)} \n');
    fprintf(fid,'.ENDS %s \n', RGname);
end


%% close the file after writing the spice file
fclose(fid);


