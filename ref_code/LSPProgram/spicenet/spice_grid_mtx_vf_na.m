                            dum=squeeze(Rvfit3(ik,:,ih));dum(ik)=0;
                            R1 = Rvfit3(ik,ik,ih)-sum(dum);
                            dum=squeeze(Lvfit3(ik,:,ih));dum(ik)=0;
                            L1 = Lvfit3(ik,ik,ih)-sum(dum);
                            dum=squeeze(Rvfit3(ik,:,ih+1));  dum(ik)=0;
                            R2 = Rvfit3(ik,ik,ih+1)-sum(dum);
                            dum=squeeze(Lvfit3(ik,:,ih+1));  dum(ik)=0;
                            L2 = Lvfit3(ik,ik,ih+1)-sum(dum);
                            
                            LC = L1+L2;
                            RC2 = LC^2*R1*R2 / (R2*L1^2+R1*L2^2);
                            RC1 = 1 / (1/(R1+R2)-1/RC2);
                            CC = 1/RC1 * L1*L2/(R1*R2)*(R1+R2)/LC;
                            
                            fprintf(fid,'R_VC%d  nV_%d  nVC_%d  %fm  TC=0,0 \n', ...
                                NV, NV-1, NV, 1e3*RC1);
                            fprintf(fid,'C_V%d  nVC_%d  nV_%d  %fuF \n', ...
                                NV, NV, NV, 1e6*CC);
                            fprintf(fid,'R_V%d  nV_%d  nV_%d  %fm  TC=0,0 \n', ...
                                NV, NV-1, NV, 1e3*RC2);
                            fprintf(fid,'L_V%d  nV_%d  nV_%d  %fuH \n', ...
                                NV, NV-1, NV, 1e6*LC);
                        end
                        
                    end
                end
                
