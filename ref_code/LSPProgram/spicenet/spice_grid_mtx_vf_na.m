                            fprintf(fid,'L_V%d  nR_%d  nV_%d  %euH \n', ...
                                NV, NR, NV, 1e6*Ltmp);
                        else % complex pole
                            vfcplx_flag = ih+1;
                            
                            dum=squeeze(Rvfit3(ik,:,ih));dum(ik)=0;
                            R1 = Rvfit3(ik,ik,ih)-sum(dum);
