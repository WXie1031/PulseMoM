                            dum=squeeze(Lvfit3(ik,:,ih));dum(ik)=0;
                            Ltmp = Lvfit3(ik,ik,ih)-sum(dum);
                            
                            fprintf(fid,'R_V%d  nR_%d  nV_%d  %em  TC=0,0 \n', ...
