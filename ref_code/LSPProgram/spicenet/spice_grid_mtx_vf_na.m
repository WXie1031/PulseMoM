                %                 fprintf(fid,'L_L%d  nV_%d  %s  %fuH \n', ...
                %                     NL, NV, nod_end_grid(ig,:), 1e6*L0fit3(ik,ig));
                
                NRG = NRG+1;
                fprintf(fid,'X%d  nV_%d  %s PARAMS: Ig=%.2f  R0=%e \n', ...
                    NRG, NV, RGname, Ig, Rgrod);
            else
