            % L
            NL = NL+1;
            fprintf(fid,'L_L%d  nM_%d  %s  %fuH \n', NL, 1, nod_end_grid(1,:), 1e6*Ls(1));
            
        end
        %% 2-b. no current measurment
    else
        Nfit_grid = find(abs(Rvfit3(1,:))>err_fit, 1, 'last');
        %nodL1 = nodR;
        if Nfit_grid>0
            % add vector fitting equivalent circuit
            vfcplx_flag = Nfit_grid+1;
            
            for ih = 1:Nfit_grid
                if ih == 1
                    NV = NV+1;
                    
                    if isreal(Rvfit3(ik,ih))  % real pole
                        fprintf(fid,'R_V%d  nR_%d  nV_%d  %fm  TC=0,0 \n', ...
                            NV, 1, NV, 1e3*Rvfit3(1,ih));
                        fprintf(fid,'L_V%d  nR_%d  nV_%d  %fuH \n', ...
                            NV, 1, NV, 1e6*Lvfit3(1,ih));
                    else % complex pole
                        vfcplx_flag = ih+1;
                        
