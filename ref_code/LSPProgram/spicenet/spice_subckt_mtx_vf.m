                        vfcplx_flag = ih+1;
                        
                        R1 = Rvfit3(ik,ih);
                        R2 = Rvfit3(ik,ih+1);
                        L1 = Lvfit3(ik,ih);
                        L2 = Lvfit3(ik,ih+1);
                        
